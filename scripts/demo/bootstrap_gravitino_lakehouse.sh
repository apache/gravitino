write_gravitino_conf() {
  local tmp_file backup_file

  mkdir -p "$(dirname "${GRAVITINO_CONF_FILE}")" "${GRAVITINO_HOME}/data"
  tmp_file="$(mktemp)"
  backup_file="${GRAVITINO_CONF_FILE}.bak.$(date +%Y%m%d%H%M%S)"

  render_gravitino_conf > "${tmp_file}"
  if [[ -f "${GRAVITINO_CONF_FILE}" ]]; then
    cp "${GRAVITINO_CONF_FILE}" "${backup_file}"
    log "Backed up existing config to ${backup_file}"
  fi
  mv "${tmp_file}" "${GRAVITINO_CONF_FILE}"
  log "Wrote ${GRAVITINO_CONF_FILE}"
}

http_status() {
  local method="$1"
  local url="$2"
  local data="${3:-}"
  local response_file

  response_file="$(mktemp)"
  if [[ -n "${data}" ]]; then
    curl -sS -X "${method}" \
      -H "${GRAVITINO_ACCEPT_HEADER}" \
      -H "${JSON_HEADER}" \
      -d "${data}" \
      -o "${response_file}" \
      -w '%{http_code}' \
      "${url}"
  else
    curl -sS -X "${method}" \
      -H "${GRAVITINO_ACCEPT_HEADER}" \
      -H "${JSON_HEADER}" \
      -o "${response_file}" \
      -w '%{http_code}' \
      "${url}"
  fi
  rm -f "${response_file}"
}

wait_for_url() {
  local url="$1"
  local attempts="${2:-40}"
  local sleep_seconds="${3:-3}"
  local status=""
  local attempt=1

  while (( attempt <= attempts )); do
    status="$(curl -sS -o /dev/null -w '%{http_code}' "${url}" || true)"
    if [[ "${status}" =~ ^[1-4][0-9][0-9]$ ]]; then
      log "Health check passed for ${url} (HTTP ${status})"
      return 0
    fi
    sleep "${sleep_seconds}"
    attempt=$((attempt + 1))
  done

  fail "Health check failed for ${url}, last HTTP status: ${status:-n/a}"
}

ensure_metalake() {
  local url="${GRAVITINO_URI}/api/metalakes/${METALAKE_NAME}"
  local status

  status="$(http_status GET "${url}")"
  if [[ "${status}" == "200" ]]; then
    log "Metalake ${METALAKE_NAME} already exists"
    return 0
  fi

  status="$(
    http_status \
      POST \
      "${GRAVITINO_URI}/api/metalakes" \
      "{\"name\":\"${METALAKE_NAME}\",\"comment\":\"Demo metalake for Spark + Iceberg REST + Lance REST\"}"
  )"
  [[ "${status}" == "200" ]] || fail "Create metalake failed with HTTP ${status}"
  log "Created metalake ${METALAKE_NAME}"
}

ensure_catalog() {
  local catalog_name="$1"
  local payload="$2"
  local status
  local url="${GRAVITINO_URI}/api/metalakes/${METALAKE_NAME}/catalogs/${catalog_name}"

  status="$(http_status GET "${url}")"
  if [[ "${status}" == "200" ]]; then
    log "Catalog ${catalog_name} already exists"
    return 0
  fi

  status="$(http_status POST "${GRAVITINO_URI}/api/metalakes/${METALAKE_NAME}/catalogs" "${payload}")"
  [[ "${status}" == "200" ]] || fail "Create catalog ${catalog_name} failed with HTTP ${status}"
  log "Created catalog ${catalog_name}"
}

ensure_lance_catalog() {
  ensure_catalog \
    "${LANCE_REST_CATALOG}" \
    "$(cat <<EOF
{"name":"${LANCE_REST_CATALOG}","type":"RELATIONAL","comment":"Lance catalog for local REST demo","provider":"lakehouse-generic","properties":{"location":"${LANCE_LOCATION}","lance.storage.endpoint":"${MINIO_ENDPOINT}","lance.storage.allow_http":"true","lance.storage.access_key_id":"${MINIO_ACCESS_KEY}","lance.storage.secret_access_key":"${MINIO_SECRET_KEY}","lance.storage.region":"${MINIO_REGION}"}}
EOF
)"
}

ensure_iceberg_catalog() {
  ensure_catalog \
    "${ICEBERG_REST_CATALOG}" \
    "$(cat <<EOF
{"name":"${ICEBERG_REST_CATALOG}","type":"RELATIONAL","comment":"Iceberg catalog for local REST demo","provider":"lakehouse-iceberg","properties":{"catalog-backend":"jdbc","uri":"${ICEBERG_JDBC_URI}","jdbc-driver":"org.h2.Driver","jdbc-user":"gravitino","jdbc-password":"gravitino","jdbc-initialize":"true","warehouse":"${ICEBERG_WAREHOUSE}","io-impl":"org.apache.iceberg.aws.s3.S3FileIO","credential-providers":"s3-secret-key","s3-endpoint":"${MINIO_ENDPOINT}","s3-region":"${MINIO_REGION}","s3-access-key-id":"${MINIO_ACCESS_KEY}","s3-secret-access-key":"${MINIO_SECRET_KEY}","s3-path-style-access":"true"}}
EOF
)"
}

verify_catalog_listing() {
  local response

  response="$(
    curl -sS -X GET \
      -H "${GRAVITINO_ACCEPT_HEADER}" \
      -H "${JSON_HEADER}" \
      "${GRAVITINO_URI}/api/metalakes/${METALAKE_NAME}/catalogs?details=true"
  )"
  printf '%s' "${response}" | grep -q "\"name\":\"${ICEBERG_REST_CATALOG}\"" \
    || fail "Catalog listing does not contain ${ICEBERG_REST_CATALOG}"
  printf '%s' "${response}" | grep -q "\"name\":\"${LANCE_REST_CATALOG}\"" \
    || fail "Catalog listing does not contain ${LANCE_REST_CATALOG}"
  log "Catalog listing contains ${ICEBERG_REST_CATALOG} and ${LANCE_REST_CATALOG}"
}

main() {
  require_command curl
  require_command cp
  require_command grep
  require_command rm
  require_path "${GRAVITINO_HOME}"
  require_path "${GRAVITINO_BIN}"

  write_gravitino_conf
  reset_iceberg_jdbc_store

  log "Restarting Gravitino from ${GRAVITINO_BIN}"
  "${GRAVITINO_BIN}" restart

  wait_for_url "${GRAVITINO_URI}/api/metalakes"
  wait_for_url "${ICEBERG_REST_HEALTH_URI}"
  wait_for_url "${LANCE_REST_URI}"

  ensure_metalake
  ensure_iceberg_catalog
  ensure_lance_catalog

  wait_for_url "${GRAVITINO_URI}/api/metalakes/${METALAKE_NAME}"
  verify_catalog_listing

  log "Bootstrap completed"
  log "Verified endpoints:"
  log "  ${GRAVITINO_URI}/api/metalakes"
  log "  ${ICEBERG_REST_HEALTH_URI}"
  log "  ${LANCE_REST_URI}"
}

main "$@"
