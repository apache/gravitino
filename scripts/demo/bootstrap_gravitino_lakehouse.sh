set -euo pipefail

GRAVITINO_HOME="${GRAVITINO_HOME:-/Users/fanng/deploy/chinamobile/package}"
GRAVITINO_CONF_FILE="${GRAVITINO_CONF_FILE:-${GRAVITINO_HOME}/conf/gravitino.conf}"
GRAVITINO_BIN="${GRAVITINO_BIN:-${GRAVITINO_HOME}/bin/gravitino.sh}"
GRAVITINO_URI="${GRAVITINO_URI:-http://127.0.0.1:8090}"
GRAVITINO_HOST="${GRAVITINO_HOST:-0.0.0.0}"
METALAKE_NAME="${METALAKE_NAME:-test}"

ICEBERG_REST_HOST="${ICEBERG_REST_HOST:-0.0.0.0}"
ICEBERG_REST_PORT="${ICEBERG_REST_PORT:-9001}"
ICEBERG_REST_URI="${ICEBERG_REST_URI:-http://127.0.0.1:${ICEBERG_REST_PORT}/iceberg/}"
ICEBERG_REST_HEALTH_URI="${ICEBERG_REST_HEALTH_URI:-http://127.0.0.1:${ICEBERG_REST_PORT}/iceberg/v1/config}"
ICEBERG_REST_CATALOG="${ICEBERG_REST_CATALOG:-iceberg}"

LANCE_REST_HOST="${LANCE_REST_HOST:-0.0.0.0}"
LANCE_REST_PORT="${LANCE_REST_PORT:-9101}"
LANCE_REST_URI="${LANCE_REST_URI:-http://127.0.0.1:${LANCE_REST_PORT}/lance}"
LANCE_REST_CATALOG="${LANCE_REST_CATALOG:-lance_catalog}"

ICEBERG_JDBC_URI="${ICEBERG_JDBC_URI:-jdbc:h2:file:${GRAVITINO_HOME}/data/iceberg_jdbc;MODE=MySQL;DB_CLOSE_DELAY=-1;AUTO_SERVER=TRUE}"
GRAVITINO_ENTITY_STORE_URI="${GRAVITINO_ENTITY_STORE_URI:-jdbc:h2:file:${GRAVITINO_HOME}/data/gravitino_jdbc;MODE=MySQL;DB_CLOSE_DELAY=-1;AUTO_SERVER=TRUE}"
ICEBERG_WAREHOUSE="${ICEBERG_WAREHOUSE:-s3://contacts/raw/iceberg}"
LANCE_LOCATION="${LANCE_LOCATION:-s3://contacts/raw/lance}"
MINIO_ENDPOINT="${MINIO_ENDPOINT:-http://127.0.0.1:9000}"
MINIO_REGION="${MINIO_REGION:-us-east-1}"
MINIO_ACCESS_KEY="${MINIO_ACCESS_KEY:-minioadmin}"
MINIO_SECRET_KEY="${MINIO_SECRET_KEY:-minioadmin}"

GRAVITINO_ACCEPT_HEADER="Accept: application/vnd.gravitino.v1+json"
JSON_HEADER="Content-Type: application/json"

log() {
  printf '[bootstrap] %s\n' "$*"
}

fail() {
  printf '[bootstrap] ERROR: %s\n' "$*" >&2
  exit 1
}

require_command() {
  command -v "$1" >/dev/null 2>&1 || fail "Required command not found: $1"
}

require_path() {
  local path="$1"
  [[ -e "${path}" ]] || fail "Required path not found: ${path}"
}

reset_iceberg_jdbc_store() {
  local deleted=false
  local jdbc_store_file

  for jdbc_store_file in "${GRAVITINO_HOME}"/data/iceberg_jdbc*; do
    if [[ -e "${jdbc_store_file}" ]]; then
      rm -f "${jdbc_store_file}"
      deleted=true
    fi
  done

  if [[ "${deleted}" == "true" ]]; then
    log "Reset Iceberg JDBC metadata store under ${GRAVITINO_HOME}/data"
  fi
}

